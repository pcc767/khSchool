package com.kh.ex01.basic;

public class PracticeVariableInfo {
	public static void main(String[] args) {
		
		// 기본형의 참조형 활용

				// byte
				System.out.println("byte의 Bit Size는? " + Byte.SIZE);
				System.out.println("byte의 byte 수는? " + Byte.SIZE / 8);
				System.out.println("byte의 Max 값은? " + Byte.MAX_VALUE);
				System.out.println("byte의 Min 값은? " + Byte.MIN_VALUE);
				System.out.println("-------------------------------");
				
				// short
				System.out.println("short의 Bit Size는? " + Short.SIZE);
				System.out.println("short의 byte 수는? " + Short.SIZE/8);
				System.out.println("short의 Max 값은? " + Short.MAX_VALUE);
				System.out.println("short의 Min 값은? " + Short.MIN_VALUE);
				System.out.println("-------------------------------");
				
				// int  -> Integer
				System.out.println("int의 Bit Size는? " + Integer.SIZE);
				System.out.println("int의 byte 수는? " + Integer.SIZE/8);
				System.out.println("int의 Max 값은? " + Integer.MAX_VALUE);
				System.out.println("int의 Min 값은? " + Integer.MIN_VALUE);
				System.out.println("-------------------------------");
				
				// long
				System.out.println("long의 Bit Size는? " + Long.SIZE);
				System.out.println("long의 byte 수는? " + Long.SIZE/8);
				System.out.println("long의 Max 값은? " + Long.MAX_VALUE);
				System.out.println("long의 Min 값은? " + Long.MIN_VALUE);
				System.out.println("-------------------------------");
				
				// float
				System.out.println("float의 Bit Size는? " + Float.SIZE);
				System.out.println("float의 byte 수는? " + Float.SIZE/8);
				System.out.println("float의 Max 값은? " + Float.MAX_VALUE);
				System.out.println("float의 Min 값은? " + Float.MIN_VALUE);
				System.out.println("-------------------------------");
				
				// double
				System.out.println("double의 Bit Size는? " + Double.SIZE);
				System.out.println("double의 byte 수는? " + Double.SIZE/8);
				System.out.println("double의 Max 값은? " + Double.MAX_VALUE);
				System.out.println("double의 Min 값은? " + Double.MIN_VALUE);
				System.out.println("-------------------------------");
				
				// char
				System.out.println("char의 Max 값은? " + (int) Character.MAX_VALUE);
				System.out.println("char의 Min 값은? " + (int) Character.MIN_VALUE);				
				System.out.println("-------------------------------");
				
				
	}
}
